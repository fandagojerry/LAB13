	<h2>Basic Laws</h2>
<p>Rugby union is played by two teams of 15 players.
Although the aim of the game is simple, there are many laws which make can make it hard for the new viewer to keep track of what is going on.

Use the ball to score more points than the other team.

You can run with the ball, kick it and pass it, but passing forwards is not allowed.

Rugby union is a contact sport, so you can tackle an opponent in order to get the ball, as long as you stay within the rules.

There is a referee, aided by two touch judges (one on each side of the pitch), to decide how the rules should be applied during a game.
</p>
<h2>How To Score(Try)</h2>
<ol>
<li>A try - five points are awarded for touching the ball down in your opponent's goal area.</li>
<li>A conversion - two points are added for a successful kick through the goalposts after a try</li>
<li>A goal kick - three points are awarded for a penalty kick or drop goal through the posts</li>
<li>If both teams score the same amount of points, or no points are scored, then the match is a draw. In some cases, extra time is played to decide who wins.</li>
</ol>

<h2>Length</h2>
<p>A game of rugby union has two periods of 40 minutes each. In international matches the referee will stop the clock for stoppages.

Between the two halves, there is a maximum 10-minute interval, after which both teams change ends.

The referee's whistle indicates the start and finish of the half.

Extra time will only be played if it's a knockout competition.
</p>

<h2>Kick-Off</h2>
<p>Before the start of the match, the referee tosses a coin to decide which team will kick off the match.

The captain of the team that wins the toss gets to decide which end he wants to attack first, or whether his side or the opposition will kick off.

The game is started by a place kick or a drop kick from the middle of the halfway line.

The ball must travel forwards at least 10 metres from the kick-off. If it does not, the opposition get the choice of a scrum or line-out on the halfway line, with the advantage of the feed or throw.

If a penalty or drop goal is scored during the game, play is restarted with a drop kick from the halfway line. The team that has conceded the points takes the kick.</p>
<h2>Ball Size</h2>
<p>Rugby union is played with an oval-shaped ball.

All balls must be between 28cm and 30cm in length (approximately 11-13 inches).

Most full-size balls weigh between 383 and 440 grams (approximately 13.5-15.5 ounces)</p>
